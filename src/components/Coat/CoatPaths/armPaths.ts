export function generateArmLeftPath(
  x05: number,
  y05: number,
  x06: number,
  y06: number,
  x10: number,
  y10: number,
  x11: number,
  y11: number
): string {
  return [
    `M ${x05} ${y05}`,
    `C ${x05 + (x06 - x05) / (35 / 9)}, ${y05 + (y06 - y05) / (26 / 7)}, ${
      x05 + (x06 - x05) / (7 / 5)
    }, ${y05 + (y06 - y05) / (13 / 10)}, ${x06} ${y06}`,
    `C ${x06 + (x10 - x06) / (5 / 3)}, ${y06 + (y10 - y06) / 7.375}, ${
      x06 + (x10 - x06) / 1.07
    }, ${y06 + (y10 - y06) / 1.475}, ${x10} ${y10}`,
    `C ${x10 + (x11 - x10) / (16 / 5)}, ${y10 + (y11 - y10) / 8}, ${
      x10 + (x11 - x10) / (4 / 3)
    }, ${y10 + (y11 - y10) / 8}, ${x11} ${y11}`,
    `C ${x11 + (x05 - x11) / 32 / 7}, ${y11 + (y05 - y11) / (11 / 3)}, ${
      x11 + (x05 - x11) / 2
    }, ${y11 + (y05 - y11) / (11 / 7)}, ${x05} ${y05}`,
  ].join(" ");
}
export function generateArmRightPath(
  x5: number,
  y5: number,
  x6: number,
  y6: number,
  x20: number,
  y20: number,
  x21: number,
  y21: number
): string {
  return [
    `M ${x5} ${y5}`,
    `C ${x5 + (x6 - x5) / (19 / 6)}, ${y5 + (y6 - y5) / (8 / 3)}, ${
      x5 + (x6 - x5) / (19 / 11)
    }, ${y5 + (y6 - y5) / (4 / 3)}, ${x6} ${y6}`,
    `C ${x6 + (x20 - x6) / (5 / 6)}, ${y6 + (y20 - y6) / 16.86}, ${
      x6 + (x20 - x6) / (5 / 4)
    }, ${y6 + (y20 - y6) / 1.31}, ${x20} ${y20}`,
    `C ${x20 + (x21 - x20) / (16 / 5)}, ${y20}, ${
      x20 + (x21 - x20) / (8 / 7)
    }, ${y20 + (y21 - y20) / 4}, ${x21} ${y21}`,
    `C ${x21 + (x5 - x21) / 4}, ${y21 + (y5 - y21) / 3.11}, ${
      x21 + (x5 - x21) / (4 / 3)
    }, ${y21 + (y5 - y21) / 1.44}, ${x5} ${y5}`,
  ].join(" ");
}
