export function generateRightVestPath(
  x0: number,
  y0: number,
  x1: number,
  y1: number,
  x2: number,
  y2: number,
  x3: number,
  y3: number,
  x4: number,
  y4: number,
  x5: number,
  y5: number,
  x6: number,
  y6: number,
  x7: number,
  y7: number
): string {
  return [
    `M ${x0} ${y0}`,
    `C ${x0 + (x1 - x0) / -1}, ${y0 + (y1 - y0) / (20 / 7)}, ${
      x0 + (x1 - x0) / 8
    }, ${y0 + (y1 - y0) / (4 / 3)}, ${x1} ${y1}`,
    `C ${x1}, ${y1}, ${x2}, ${y2}, ${x2} ${y2}`,
    `C ${x2 + (x3 - x2) / 2}, ${y2 + (y3 - y2) / 4}, ${
      x2 + (x3 - x2) / (8 / 7)
    }, ${y2 + (y3 - y2) / (8 / 5)}, ${x3} ${y3}`,
    `C ${x3 + (x4 - x3) / -13}, ${y3 + (y4 - y3) / (20 / 11)}, ${
      x3 + (x4 - x3) / (13 / 18)
    }, ${y3 + (y4 - y3) / (5 / 4)}, ${x4} ${y4}`,
    `C ${x4 + (x5 - x4) / (29 / 4)}, ${y4 + (y5 - y4) / (20 / 9)}, ${
      x4 + (x5 - x4) / (29 / 12)
    }, ${y4 + (y5 - y4) / (14 / 11)}, ${x5} ${y5}`,
    `C ${x5 + (x6 - x5) / 48}, ${y5 + (y6 - y5) / 5.03}, ${
      x5 + (x6 - x5) / 2
    }, ${y5 + (y6 - y5) / 1.25}, ${x6} ${y6}`,
    `C ${x6 + (x7 - x6) / (41 / 11)}, ${y6 + (y7 - y6) / (20 / 7)}, ${
      x6 + (x7 - x6) / (41 / 29)
    }, ${y6 + (y7 - y6) / (5 / 4)}, ${x7} ${y7}`,
    `C ${x7}, ${y7 + (y0 - y7) / (11 / 3)}, ${x7 + (x0 - x7) / 2}, ${
      y7 + (y0 - y7) / (22 / 17)
    }, ${x0} ${y0}`,
  ].join(" ");
}
