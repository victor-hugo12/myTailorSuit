export function generateRightCoatPath(
  x0: number,
  y0: number,
  x1: number,
  y1: number,
  x2: number,
  y2: number,
  x3: number,
  y3: number,
  x4: number,
  y4: number,
  x5: number,
  y5: number,
  x6: number,
  y6: number,
  x7: number,
  y7: number
): string {
  return [
    `M ${x0} ${y0}`,
    `C ${x0 + (x1 - x0) / -1}, ${y0 + (y1 - y0) / (20 / 7)}, ${
      x0 + (x1 - x0) / 8
    }, ${y0 + (y1 - y0) / (4 / 3)}, ${x1} ${y1}`,
    `C ${x1 + (x2 - x1) / 8}, ${y1 + (y2 - y1) / (8 / 3)}, ${
      x1 + (x2 - x1) / (32 / 11)
    }, ${y1 + (y2 - y1) / (8 / 7)}, ${x2} ${y2}`,
    `C ${x2 + (x3 - x2) / 1.35}, ${y2 + (y3 - y2) / -2}, ${
      x2 + (x3 - x2) / 3.93
    }, ${y2 + (y3 - y2) / -4}, ${x3} ${y3}`,
    `C ${x3 + (x4 - x3) / 15}, ${y3 + (y4 - y3) / (17 / 6)}, ${
      x3 + (x4 - x3) / (5 / 4)
    }, ${y3 + (y4 - y3) / (17 / 11)}, ${x4} ${y4}`,
    `C ${x4 + (x5 - x4) / (-7 / 3)}, ${y4 + (y5 - y4) / 2.08}, ${
      x4 + (x5 - x4) / (7 / 2)
    }, ${y4 + (y5 - y4) / 1.39}, ${x5} ${y5}`,
    `C ${x5 + (x6 - x5) / (19 / 6)}, ${y5 + (y6 - y5) / (8 / 3)}, ${
      x5 + (x6 - x5) / (19 / 11)
    }, ${y5 + (y6 - y5) / (4 / 3)}, ${x6} ${y6}`,
    `C ${x6 + (x7 - x6) / (41 / 11)}, ${y6 + (y7 - y6) / (20 / 7)}, ${
      x6 + (x7 - x6) / (41 / 29)
    }, ${y6 + (y7 - y6) / (5 / 4)}, ${x7} ${y7}`,
    `C ${x7}, ${y7}, ${x0}, ${y0}, ${x0} ${y0}`,
  ].join(" ");
}
